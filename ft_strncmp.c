#include "libft.h"

int	ft_strncmp(char *x, char *y, int n)
{
	int	i;
	int	dif;

	i = 0;
	dif = 0;
	while ((dif == 0) && (i <= n) && (x[i] != '\0'))
	{
		dif = x[i] - y[i];
		i++;
	}
	return (dif);
}
