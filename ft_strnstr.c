#include "libft.h"

char	*ft_strnstr(char *x, char *y, int len)
{
	int	i;
	int	j;

	i = 0;
	while (x[i] != '\0' && i <= len)
	{
		j = 0;
		if (x[i] == y[j])
		{
			while (x[i + j] != '\0' && x[i + j] == y[j] && i + j <= len)
			{
				if (y[j + 1] == '\0')
				{
					return (x + i);
				}
				j++;
			}
		}
		i++;
	}
	return (0);
}
