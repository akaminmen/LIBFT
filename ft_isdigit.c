int	ft_isdigit(char *x)
{
	int	counter;

	counter = 0;
	while (x[counter] != '\0')
	{
		if (!((x[counter] >= '0' && x[counter] <= '9')))
		{
			return (1);
		}
		counter++;
	}
	return (0);
}
